# Problem Statement:
#
# When working with collections such as lists, sets, and
# dictionaries, programmers often need to perform repetitive
# operations like filtering items, transforming values, or
# creating new collections.
#
# Traditionally, these operations are performed using loops.
# However, Python provides a more concise and expressive
# way to achieve the same results using **Comprehensions**.



# ============================================================
# What are Comprehensions?
# ============================================================

# Comprehensions are a compact syntax used to create
# collections in Python using a single line of code.
#
# They provide a shorter and often more readable
# alternative to writing loops.


# Important Note:
#
# Anything that can be done using comprehensions can also
# be implemented using standard loops. Comprehensions
# simply provide a cleaner and more Pythonic way of
# expressing the same logic.


# Example idea (conceptual only):
#
# Instead of writing multiple lines with loops,
# comprehensions allow developers to build collections
# in a single expression.



# ============================================================
# Why Comprehensions are Important
# ============================================================

# Comprehensions are widely used in production-level
# Python code for several reasons:
#
# 1. Cleaner Code
#    They make the code shorter and easier to read.
#
# 2. Faster Execution
#    In many cases, comprehensions are optimized and
#    run faster than equivalent loop-based code.
#
# 3. Expressive and Pythonic
#    Python developers prefer concise and expressive
#    code patterns, and comprehensions are a key
#    part of Python's design philosophy.


# Note:
# Beginners may initially find comprehensions difficult
# to understand because multiple operations can happen
# within a single line of code. However, once mastered,
# they significantly improve coding efficiency.



# ============================================================
# Real-World Use Cases of Comprehensions
# ============================================================

# Comprehensions are frequently used in real-world
# applications. Some common scenarios include:


# 1. Filtering Items
#
# Example:
# Selecting only free courses or top-rated courses
# in an e-learning platform.


# 2. Transforming Data
#
# Example:
# Converting product prices from BDT to USD
# in an online store.


# 3. Creating New Collections
#
# Example:
# Generating a list of weekly or monthly
# top-selling books in a bookstore system.


# 4. Flattening Nested Structures
#
# Example:
# Extracting ingredients from nested lists or
# dictionaries representing a complex tea recipe.



# ============================================================
# Types of Comprehensions in Python
# ============================================================

# Python provides four major types of comprehensions:


# 1. List Comprehension
#    Used to create lists.


# 2. Set Comprehension
#    Used to create sets.


# 3. Dictionary Comprehension
#    Used to create dictionaries.


# 4. Generator Expression
#    Used to create generators that produce values
#    lazily (one at a time instead of all at once).



